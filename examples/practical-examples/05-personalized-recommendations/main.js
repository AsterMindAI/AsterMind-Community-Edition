/**
 * Initialize item embeddings
 * Creates embeddings for all items based on their features
 */
async function initializeItemEmbeddings() {
    console.log('🔧 Initializing item embeddings...');
    
    // Create ELM encoder for items
    // Train on classification task, then use hidden layer as embeddings
    // Let ELM create its own encoder to ensure consistency
    itemEncoder = new ELM({
        categories: ITEMS.map((_, i) => `item-${i}`), // Each item as a category
        hiddenUnits: 128,
        maxLen: 50,
        useTokenizer: true, // Enable text mode
        charSet: 'abcdefghijklmnopqrstuvwxyz0123456789 -',
        tokenizerDelimiter: /\s+/,
        activation: 'relu',
        weightInit: 'xavier',
        ridgeLambda: 0.01,
    });
    
    // Get ELM's internal encoder - this is the one that will be used for embeddings
    const textEncoder = itemEncoder['encoder'];
    if (!textEncoder) {
        throw new Error('Encoder not initialized');
    }
    
    // Get the expected vector size
    const expectedVectorSize = textEncoder.getVectorSize();
    console.log(`📏 Expected vector size: ${expectedVectorSize} dimensions`);
    
    // Prepare training data
    // Use item features (title + category + tags) as training examples
    const trainingTexts = ITEMS.map(item => {
        const features = `${item.title} ${item.category} ${item.tags.join(' ')}`;
        return features.toLowerCase();
    });
    
    // Encode texts and create one-hot labels
    const X = [];
    const Y = [];
    
    // Helper to encode and ensure consistent size
    const encodeAndPad = (text) => {
        const vec = textEncoder.normalize(textEncoder.encode(text));
        // Ensure vector is exactly the expected size
        if (vec.length !== expectedVectorSize) {
            console.warn(`⚠️ Vector size mismatch: got ${vec.length}, expected ${expectedVectorSize}. Fixing...`);
            const fixed = vec.slice(0, expectedVectorSize);
            while (fixed.length < expectedVectorSize) {
                fixed.push(0);
            }
            return fixed;
        }
        return vec;
    };
    
    trainingTexts.forEach((text, i) => {
        X.push(encodeAndPad(text));
        Y.push(itemEncoder.oneHot(ITEMS.length, i)); // Each item is its own category
    });
    
    // Verify all vectors have consistent size
    const firstSize = X[0]?.length;
    if (!firstSize || firstSize !== expectedVectorSize) {
        throw new Error(`Vector size mismatch: first vector is ${firstSize}, expected ${expectedVectorSize}`);
    }
    
    const inconsistent = X.find((v, i) => v.length !== firstSize);
    if (inconsistent) {
        const badIndex = X.findIndex(v => v.length !== firstSize);
        throw new Error(`Vector ${badIndex} has size ${X[badIndex].length}, expected ${firstSize}`);
    }
    
    console.log(`📚 Training encoder on ${X.length} items (all vectors: ${firstSize} dimensions)...`);
    const startTime = performance.now();
    
    itemEncoder.trainFromData(X, Y);
    
    const trainTime = performance.now() - startTime;
    console.log(`✅ Training complete in ${trainTime.toFixed(2)}ms`);
    
    // Build embedding store for all items
    buildItemEmbeddingStore();
    
    isReady = true;
    console.log('🚀 Recommendation system ready!');
}

/**
 * Build the embedding store with all item embeddings
 */
function buildItemEmbeddingStore() {
    console.log('📦 Building item embedding store...');
    
    itemEmbeddingStore = new EmbeddingStore(128, {
        capacity: 1000,
        storeUnit: true, // Normalize for cosine similarity
    });
    
    // Get encoder and expected vector size
    const textEncoder = itemEncoder['encoder'];
    if (!textEncoder) {
        throw new Error('Encoder not initialized');
    }
    
    const expectedVectorSize = textEncoder.getVectorSize();
    
    // Helper to encode and ensure consistent size
    const encodeAndPad = (text) => {
        const vec = textEncoder.normalize(textEncoder.encode(text.toLowerCase()));
        // Ensure vector is exactly the expected size
        if (vec.length !== expectedVectorSize) {
            console.warn(`⚠️ Vector size mismatch for "${text.substring(0, 30)}...": got ${vec.length}, expected ${expectedVectorSize}. Fixing...`);
            const fixed = vec.slice(0, expectedVectorSize);
            while (fixed.length < expectedVectorSize) {
                fixed.push(0);
            }
            return fixed;
        }
        return vec;
    };
    
    // Generate and store embeddings for all items
    ITEMS.forEach(item => {
        const features = `${item.title} ${item.category} ${item.tags.join(' ')}`;
        const vec = encodeAndPad(features);
        
        // Get embedding from ELM
        const embeddingBatch = itemEncoder.getEmbedding([vec]);
        
        // Ensure we have a valid embedding
        if (!Array.isArray(embeddingBatch) || !embeddingBatch[0] || !Array.isArray(embeddingBatch[0])) {
            console.error(`Failed to get embedding for item ${item.id}`);
            return;
        }
        
        const embedding = Array.from(embeddingBatch[0]); // Ensure it's a plain array
        
        // Verify embedding size matches store dimension
        if (embedding.length !== 128) {
            console.warn(`⚠️ Embedding size ${embedding.length} doesn't match store dimension 128. Padding/truncating...`);
            const fixed = embedding.slice(0, 128);
            while (fixed.length < 128) {
                fixed.push(0);
            }
            itemEmbeddingStore.add({
                id: `item-${item.id}`,
                vec: fixed,
                meta: item,
            });
        } else {
            itemEmbeddingStore.add({
                id: `item-${item.id}`,
                vec: embedding,
                meta: item,
            });
        }
    });
    
    console.log(`✅ Stored ${itemEmbeddingStore.size()} item embeddings`);
}

/**
 * Build user profile embedding from interactions
 * Averages embeddings of liked items to create user preference vector
 */
function buildUserProfile() {
    if (userInteractions.liked.size === 0) {
        userProfileEmbedding = null;
        return;
    }
    
    console.log('👤 Building user profile...');
    
    // Get embeddings of all liked items
    const likedItemIds = Array.from(userInteractions.liked);
    const likedEmbeddings = likedItemIds
        .map(id => {
            const itemId = `item-${id}`;
            const record = itemEmbeddingStore.get(itemId);
            // Use raw vector if available, otherwise use normalized vector
            return record ? (record.raw ? Array.from(record.raw) : Array.from(record.vec)) : null;
        })
        .filter(emb => emb !== null);
    
    if (likedEmbeddings.length === 0) {
        userProfileEmbedding = null;
        return;
    }
    
    // Average the embeddings to create user profile
    // This represents the user's preference vector
    const embeddingDim = likedEmbeddings[0].length;
    userProfileEmbedding = new Array(embeddingDim).fill(0);
    
    likedEmbeddings.forEach(embedding => {
        embedding.forEach((val, i) => {
            userProfileEmbedding[i] += val;
        });
    });
    
    // Average
    userProfileEmbedding = userProfileEmbedding.map(val => val / likedEmbeddings.length);
    
    // Normalize the profile vector
    const magnitude = Math.sqrt(
        userProfileEmbedding.reduce((sum, val) => sum + val * val, 0)
    );
    if (magnitude > 0) {
        userProfileEmbedding = userProfileEmbedding.map(val => val / magnitude);
    }
    
    console.log(`✅ User profile built from ${likedEmbeddings.length} liked items`);
}

/**
 * Generate recommendations based on user profile
 * @param {number} topK - Number of recommendations to return
 * @returns {Array} Array of recommended items with scores
 */
function generateRecommendations(topK = 6) {
    if (!isReady || !userProfileEmbedding) {
        return [];
    }
    
    // Get all items user hasn't interacted with
    const allItemIds = ITEMS.map(item => item.id);
    const unviewedItemIds = allItemIds.filter(
        id => !userInteractions.viewed.has(id) && !userInteractions.liked.has(id)
    );
    
    if (unviewedItemIds.length === 0) {
        return [];
    }
    
    // Find similar items using cosine similarity
    const recommendations = itemEmbeddingStore.query(
        userProfileEmbedding, // Query vector (first parameter)
        Math.min(topK * 2, itemEmbeddingStore.size()), // Get more candidates
        { metric: 'cosine' } // Options: Use cosine similarity
    );
    
    // Filter out items user has already interacted with
    const filtered = recommendations
        .filter(hit => {
            // Handle both cases: hit.meta.id might be a number or string
            let itemId;
            if (typeof hit.meta.id === 'number') {
                itemId = hit.meta.id;
            } else if (typeof hit.meta.id === 'string') {
                // If it's a string like "item-1", extract the number
                itemId = parseInt(hit.meta.id.replace('item-', ''));
            } else {
                // Fallback: try to get id from hit.id or hit.meta
                const idStr = hit.id || hit.meta?.id || '';
                itemId = parseInt(idStr.toString().replace('item-', ''));
            }
            
            // Skip if we couldn't parse a valid ID
            if (isNaN(itemId)) {
                console.warn('Could not parse item ID from:', hit);
                return false;
            }
            
            return !userInteractions.viewed.has(itemId) && !userInteractions.liked.has(itemId);
        })
        .slice(0, topK); // Take top K
    
    return filtered.map(hit => ({
        ...hit.meta,
        score: hit.score,
        reason: getRecommendationReason(hit.meta, hit.score),
    }));
}

/**
 * Get a human-readable reason for the recommendation
 */
function getRecommendationReason(item, score) {
    const percent = Math.round(score * 100);
    
    // Find common tags with liked items
    const likedItems = Array.from(userInteractions.liked)
        .map(id => ITEMS.find(item => item.id === id))
        .filter(Boolean);
    
    const likedTags = new Set();
    likedItems.forEach(item => {
        item.tags.forEach(tag => likedTags.add(tag));
    });
    
    const commonTags = item.tags.filter(tag => likedTags.has(tag));
    
    if (commonTags.length > 0) {
        return `Similar tags: ${commonTags.slice(0, 2).join(', ')}`;
    } else if (likedItems.length > 0) {
        const likedCategory = likedItems[0].category;
        if (item.category === likedCategory) {
            return `Same category: ${item.category}`;
        }
    }
    
    return `${percent}% match to your preferences`;
}

