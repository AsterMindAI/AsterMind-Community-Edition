/**
 * Render all items in the browse grid
 */
function renderItems() {
    const itemGrid = document.getElementById('itemGrid');
    itemGrid.innerHTML = '';
    
    ITEMS.forEach(item => {
        const card = document.createElement('div');
        card.className = 'item-card';
        
        // Mark if interacted
        if (userInteractions.liked.has(item.id)) {
            card.classList.add('interacted');
        }
        
        card.innerHTML = `
            <div class="item-title">${item.title}</div>
            <div class="item-category">${item.category}</div>
            <div class="item-tags">
                ${item.tags.map(tag => `<span class="item-tag">${tag}</span>`).join('')}
            </div>
        `;
        
        // Click handler
        card.addEventListener('click', () => {
            handleItemInteraction(item.id);
        });
        
        itemGrid.appendChild(card);
    });
}

/**
 * Render recommendations
 */
function renderRecommendations() {
    const recommendationGrid = document.getElementById('recommendationGrid');
    
    if (!userProfileEmbedding) {
        recommendationGrid.innerHTML = `
            <div style="padding: 2rem; text-align: center; color: #6b7280; grid-column: 1 / -1;">
                Like some items above to see personalized recommendations!
            </div>
        `;
        return;
    }
    
    const recommendations = generateRecommendations(6);
    
    if (recommendations.length === 0) {
        recommendationGrid.innerHTML = `
            <div style="padding: 2rem; text-align: center; color: #6b7280; grid-column: 1 / -1;">
                You've viewed all items! Try resetting to see recommendations again.
            </div>
        `;
        return;
    }
    
    recommendationGrid.innerHTML = '';
    
    recommendations.forEach(item => {
        const card = document.createElement('div');
        card.className = 'recommendation-card';
        
        const scorePercent = Math.round(item.score * 100);
        
        card.innerHTML = `
            <div class="recommendation-score">${scorePercent}% match</div>
            <div class="recommendation-title">${item.title}</div>
            <div class="recommendation-category">${item.category}</div>
            <div class="recommendation-reason">${item.reason}</div>
        `;
        
        recommendationGrid.appendChild(card);
    });
}

/**
 * Update statistics display
 */
function updateStats() {
    document.getElementById('viewedCount').textContent = userInteractions.viewed.size;
    document.getElementById('likedCount').textContent = userInteractions.liked.size;
    
    const recommendations = userProfileEmbedding ? generateRecommendations(6) : [];
    document.getElementById('recommendationCount').textContent = recommendations.length;
}

/**
 * Handle item interaction (click)
 */
function handleItemInteraction(itemId) {
    // Toggle like status
    if (userInteractions.liked.has(itemId)) {
        userInteractions.liked.delete(itemId);
    } else {
        userInteractions.liked.add(itemId);
    }
    
    // Always mark as viewed
    userInteractions.viewed.add(itemId);
    
    // Rebuild user profile
    buildUserProfile();
    
    // Update UI
    renderItems();
    renderRecommendations();
    updateStats();
    
    console.log(`📊 Interactions updated: ${userInteractions.liked.size} liked, ${userInteractions.viewed.size} viewed`);
}

/**
 * Reset all interactions
 */
window.resetInteractions = function() {
    if (confirm('Reset all interactions? This will clear your preferences.')) {
        userInteractions.viewed.clear();
        userInteractions.liked.clear();
        userProfileEmbedding = null;
        
        renderItems();
        renderRecommendations();
        updateStats();
        
        console.log('🔄 Interactions reset');
    }
};

